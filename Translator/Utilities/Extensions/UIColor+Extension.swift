import UIKit
extension UIColor {
    static var translateBlue: UIColor {
        return #colorLiteral(red: 0, green: 0.4862745098, blue: 0.9137254902, alpha: 1)
    }
    static var translateRed: UIColor {
        return #colorLiteral(red: 0.9294117647, green: 0.2980392157, blue: 0.3607843137, alpha: 1)
    }
    static var mainText: UIColor {
        return #colorLiteral(red: 0.9803921569, green: 0.9803921569, blue: 0.9803921569, alpha: 1)
    }
    static var lightGray: UIColor {
        return #colorLiteral(red: 0.9803921569, green: 0.9803921569, blue: 0.9803921569, alpha: 0.8)
    }
}
